require "spec_helper"

describe EnquiriesController do
  describe "routing" do

    it "routes to #index" do
      get("/enquiries").should route_to("enquiries#index")
    end

    it "routes to #new" do
      get("/enquiries/new").should route_to("enquiries#new")
    end

    it "routes to #show" do
      get("/enquiries/1").should route_to("enquiries#show", :id => "1")
    end

    it "routes to #edit" do
      get("/enquiries/1/edit").should route_to("enquiries#edit", :id => "1")
    end

    it "routes to #create" do
      post("/enquiries").should route_to("enquiries#create")
    end

    it "routes to #update" do
      put("/enquiries/1").should route_to("enquiries#update", :id => "1")
    end

    it "routes to #destroy" do
      delete("/enquiries/1").should route_to("enquiries#destroy", :id => "1")
    end

  end
end
